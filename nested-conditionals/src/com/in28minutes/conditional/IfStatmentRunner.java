package com.in28minutes.conditional;

class IfStatmentRunner {

	public static void main(String[] args) {
		int i = 26;
		if(i == 25) {
			System.out.println("i is equal to 25");
		} else if(i == 24) {
			System.out.println("i is equal to 24");
		}
		else {
			System.out.println("is is NOT equal to 25 nor 24");
		}
	}

}
